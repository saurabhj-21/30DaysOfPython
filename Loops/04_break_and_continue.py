for i in range(100):
    if i == 40:
        break  #Exit the loop when i is 40
    print(i)
print("Loop terminated")
# The above code prints numbers from 0 to 39 using a for loop and the break statement.

for j in range(100):
    if j == 40:
        continue #Skip the rest of the loop when j is 40
    print(j)
print("Loop terminated")
# The above code prints numbers from 0 to 99 using a for loop and the continue statement.