#for loop in lists
my_list =[1, "Rohan", False, "Hello", 5.6, 3.14, True, "Python"]

for i in my_list:
    print(i)
# The above code prints the content of a list using a for loop.


#for loop in tuples
my_tuple = (1, "Rohan", False, "Hello", 5.6, 3.14, True, "Python")
for i in my_tuple:
    print(i)
# The above code prints the content of a tuple using a for loop.


#for loop in strings
s = "Rohan"
for i in s:
    print(i)
# The above code prints the content of a string using a for loop.